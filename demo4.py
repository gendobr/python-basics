a = 5
b = 2

# Порівняння
c = a == b
print(c)  #  False  = "Хибне твердження"

d = a != b
print(d)  #  True  = "Істинне твердження"


d = a > b
print(d)  #  True  = "Істинне твердження"


d = a < b
print(d)  #  False  = "Хибне твердження"

d = a >= b
print(d)  #  True  = "Істинне твердження"


d = a <= b
print(d)  #  False  = "Хибне твердження"


# and or not
a = 5
b = 2
c = 7

d = (a <= b) or (b <= c)
print(d)

d = (a <= b) and (b <= c)
print(d)


d = not (a <= b) and (b <= c)
print(d)


